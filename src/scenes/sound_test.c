static s16 sSoundTestCursor = 0;


static void sound_test_play_current(void) {
    stop_all_soundplayers();
    play_sound(sSoundTestTracks[gSoundTest->cursor]);
}


// Scene Start
void sound_test_scene_start(void *sVar, s32 dArg) {
    struct TextPrinter *printer;

    func_08007324(FALSE);
    func_080073f0();

    printer = text_printer_create_new(get_current_mem_id(), 3, 208, 32);
    text_printer_set_x_y(printer, 120, 20);
    text_printer_set_layer(printer, 0x4800);
    text_printer_set_alignment(printer, TEXT_ALIGNMENT_CENTER);
    text_printer_center_by_content(printer, TRUE);
    text_printer_set_palette(printer, 12);
    text_printer_set_colors(printer, 0);
    text_printer_set_line_spacing(printer, 15);
    text_printer_set_string(printer, "SOUND TEST");
    gSoundTest->titleText = printer;

    printer = text_printer_create_new(get_current_mem_id(), 3, 208, 32);
    text_printer_set_x_y(printer, 120, 80);
    text_printer_set_layer(printer, 0x4800);
    text_printer_set_alignment(printer, TEXT_ALIGNMENT_CENTER);
    text_printer_center_by_content(printer, TRUE);
    text_printer_set_palette(printer, 12);
    text_printer_set_colors(printer, 0);
    text_printer_set_line_spacing(printer, 15);
    text_printer_set_string(printer, sSoundTestTrackNames[sSoundTestCursor]);
    gSoundTest->trackText = printer;

    gSoundTest->cursor = sSoundTestCursor;
    gSoundTest->totalTracks = SOUND_TEST_TOTAL_TRACKS;

    sound_test_play_current();
}


// Scene Update (Paused)
void sound_test_scene_paused(void *sVar, s32 dArg) {
}


// Scene Update (Active)
void sound_test_scene_update(void *sVar, s32 dArg) {
    if (!sound_test_scene_inputs_enabled()) {
        text_printer_update(gSoundTest->titleText);
        text_printer_update(gSoundTest->trackText);
        return;
    }

    if (D_03004afc & DPAD_DOWN) {
        gSoundTest->cursor++;
        if (gSoundTest->cursor >= SOUND_TEST_TOTAL_TRACKS) {
            gSoundTest->cursor = SOUND_TEST_TOTAL_TRACKS - 1;
        }
        sSoundTestCursor = gSoundTest->cursor;
        text_printer_set_string(gSoundTest->trackText, sSoundTestTrackNames[gSoundTest->cursor]);
        sound_test_play_current();
    } else if (D_03004afc & DPAD_UP) {
        gSoundTest->cursor--;
        if (gSoundTest->cursor < 0) {
            gSoundTest->cursor = 0;
        }
        sSoundTestCursor = gSoundTest->cursor;
        text_printer_set_string(gSoundTest->trackText, sSoundTestTrackNames[gSoundTest->cursor]);
        sound_test_play_current();
    } else if (D_03004afc & B_BUTTON) {
        stop_all_soundplayers();
        gSoundTest->inputsEnabled = FALSE;
        set_pause_beatscript_scene(FALSE);
        play_sound(&s_menu_cancel3_seqData);
    }

    text_printer_update(gSoundTest->titleText);
    text_printer_update(gSoundTest->trackText);
}


// Check if Scene Can Receive Inputs
u32 sound_test_scene_inputs_enabled(void) {
    return gSoundTest->inputsEnabled ? TRUE : FALSE;
}


// Scene Stop
void sound_test_scene_stop(void *sVar, s32 dArg) {
    func_08008628();
    func_08004058();
}
